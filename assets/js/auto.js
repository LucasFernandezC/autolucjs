class Auto {
    constructor(marca, anio, tipo, puertas, combustible, aire, esp, techo, img, id, precio) {
    this.marca = marca;
    this.anio = parseInt(anio);
    this.tipo = tipo;
    this.puertas = puertas;
    this.combustible = combustible;
    this.aire = aire;
    this.esp = esp;
    this.techo = techo;
    this.img = img;
    this.id = id;
    this.precio = precio; 
}


}